class AssignmentOperators{
	public static void main(String[] args){

	int a=20;
	System.out.println("a:"+a);
	
	a+=10;
	System.out.println("a:"+a);
	
	a*=10;
	System.out.println("a:"+a);
	
	a-=20;
	System.out.println("a:"+a);

	a/=10;
	System.out.println("a:"+a);

	a%=2;
	System.out.println("a:"+a);

}
}