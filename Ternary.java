class Ternary {
	public static void main(String[] args) {

	 int a=20;
	 int b=30;

	 int max= a>b ? a:b;

	 System.out.println("Largest of "+a+" and "+b+" is "+max);

	 System.out.println("---------------");

	 int x=20;
	 int y=30;

	 int min = x<y ? x:y;

	 System.out.println("Smallest of "+x+" and "+y+" is "+min);
	}
}